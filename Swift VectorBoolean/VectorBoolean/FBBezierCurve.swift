// 372
func FBConvexHullBuildFromPoints(inPoints: [CGPoint]) -> (hull: [CGPoint], hullLength: Int) {

  // Compute the convex hull for this bezier curve. The convex hull is made up of the end and control points.
  //  The hard part is determine the order they go in, and if any are inside or colinear with the convex hull.

  // Uses the Monotone chain algorithm:
  //  http://en.wikibooks.org/wiki/Algorithm_Implementation/Geometry/Convex_hull/Monotone_chain

  // Start with all the end and control points in any order.
  let numberOfPoints = 4

  var points = inPoints

  // Sort points ascending x, if equal compare y
  //  Bubble sort, which should be ok with a max of 4 elements, and the fact that our one current use case
  //  already has them in ascending X order (i.e. should be just comparisons to verify)
  var sortLength = numberOfPoints
  do {
    var newSortLength = 0
    for var i = 1; i < sortLength; i++ {
      if ( points[i - 1].x > points[i].x || (FBAreValuesClose(points[i - 1].x, points[i].x) && points[i - 1].y > points[i].y) ) {
        let tempPoint = points[i]
        points[i] = points[i - 1]
        points[i - 1] = tempPoint
        newSortLength = i
      }
    }
    sortLength = newSortLength
  } while sortLength > 0

  // Create the results
  var filledInIx = 0
  var results = [CGPoint](count: 8, repeatedValue: CGPointZero)

  // Build lower hull
  for var i = 0; i < numberOfPoints; i++ {
    while filledInIx >= 2 && FBConvexHullDoPointsTurnWrongDirection(results[filledInIx - 2], results[filledInIx - 1], points[i]) {
      --filledInIx
    }
    results[filledInIx] = points[i];
    ++filledInIx;
  }

  // Build upper hull
  var thresholdIndex = filledInIx + 1
  for var i = numberOfPoints - 2; i >= 0; i-- {
    while filledInIx >= thresholdIndex && FBConvexHullDoPointsTurnWrongDirection(results[filledInIx - 2], results[filledInIx - 1], points[i]) {
      --filledInIx
    }

    results[filledInIx] = points[i];
    ++filledInIx;
  }

  return (hull: results, hullLength: filledInIx - 1)
}

