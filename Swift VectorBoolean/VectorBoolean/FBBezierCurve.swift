  let baseY = FBGaussQuadratureBaseForCubic(t, p1.y, p2.y, p3.y, p4.y)

  return CGFloat(sqrtf(Float(baseX * baseX + baseY * baseY)))
}

func FBGaussQuadratureComputeCurveLengthForCubic(z: CGFloat, steps: Int, p1: CGPoint, p2: CGPoint, p3: CGPoint, p4: CGPoint) -> CGFloat
{
  let z2 = z / 2.0
  var sum : CGFloat = 0.0
  for var i = 0; i < steps; i++ {
    let correctedT = z2 * FBLegendreGaussAbscissaeValues[steps][i] + z2
    sum += FBLegendreGaussWeightValues[steps][i] * FBGaussQuadratureFOfTForCubic(correctedT, p1, p2, p3, p4)
  }
  return z2 * sum
}

func FBSign(value: CGFloat) -> Int
{
  if value < 0.0 {
    return -1
  } else {
    return 1
  }
}

func FBCountBezierCrossings(bezierPoints: [CGPoint], degree: Int) -> Int {

  var count : Int = 0
  var sign = FBSign(bezierPoints[0].y)

  var previousSign = sign
  for var i : Int = 1; i <= degree; i++ {
    sign = FBSign(bezierPoints[i].y)
    if sign != previousSign {
      count++
    }
    previousSign = sign;
  }
  return count
}

let FBFindBezierRootsMaximumDepth = 64

// FBIsControlPolygonFlatEnough Usage:
// var intersectAt = CGPoint(x:0,y:0)
// var boolGotit = FBIsControlPolygonFlatEnough(points,degree,&intersectAt)
func FBIsControlPolygonFlatEnough(bezierPoints: [CGPoint], degree: Int, inout intersectionPoint: CGPoint) -> Bool {

  let FBFindBezierRootsErrorThreshold = CGFloat(ldexpf(Float(1.0), Int32(-1 * (FBFindBezierRootsMaximumDepth - 1))))

  let line = FBNormalizedLineMake(bezierPoints[0], bezierPoints[degree])

  // Find the bounds around the line
  var belowDistance = CGFloat(0)
  var aboveDistance = CGFloat(0)
  for var i = 1; i < degree; i++ {
    let distance = FBNormalizedLineDistanceFromPoint(line, bezierPoints[i])
    if distance > aboveDistance {
      aboveDistance = distance
    }

    if distance < belowDistance {
      belowDistance = distance
    }
  }

  let zeroLine = FBNormalizedLineMakeWithCoefficients(0, 1, 0)
  let aboveLine = FBNormalizedLineOffset(line, -aboveDistance)
  let intersect1 = FBNormalizedLineIntersection(zeroLine, aboveLine)

  let belowLine = FBNormalizedLineOffset(line, -belowDistance)
  let intersect2 = FBNormalizedLineIntersection(zeroLine, belowLine)

  let error = max(intersect1.x, intersect2.x) - min(intersect1.x, intersect2.x)
  if error < FBFindBezierRootsErrorThreshold {
    intersectionPoint = FBNormalizedLineIntersection(zeroLine, line)
    return true
  }

  return false
}

// 330
func FBFindBezierRootsWithDepth(bezierPoints: [CGPoint], degree: Int, depth: Int, perform: (root: CGFloat) -> Void) {

  let crossingCount = FBCountBezierCrossings(bezierPoints, degree)
  if crossingCount == 0 {
    return
  }
  else if crossingCount == 1 {
    if depth >= FBFindBezierRootsMaximumDepth {
      let root = (bezierPoints[0].x + bezierPoints[degree].x) / 2.0
      perform(root: root)
      return
    }
    var intersectionPoint = CGPointZero
    if FBIsControlPolygonFlatEnough(bezierPoints, degree, &intersectionPoint) {
      perform(root: intersectionPoint.x)
      return
    }
  }

  // Subdivide and try again
  let bwp = BezierWithPoints(degree, bezierPoints, 0.5, true)
  FBFindBezierRootsWithDepth(bwp.leftCurve!, degree, depth + 1, perform)
  FBFindBezierRootsWithDepth(bwp.rightCurve!, degree, depth + 1, perform)
}

