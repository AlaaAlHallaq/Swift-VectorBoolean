// FBBezierCurve is one cubic 2D bezier curve.
// It represents one segment of a bezier path, and is where
// the intersection calculation happens

import UIKit


// MARK ---- Normalized Line ----

struct FBNormalizedLine {
  var a : CGFloat // * x +
  var b : CGFloat // * y +
  var c : CGFloat // constant
}


// Create a normalized line such that computing the distance from it is quick.
//  See:    http://softsurfer.com/Archive/algorithm_0102/algorithm_0102.htm#Distance%20to%20an%20Infinite%20Line
//          http://www.cs.mtu.edu/~shene/COURSES/cs3621/NOTES/geometry/basic.html
//
 func FBNormalizedLineMake(point1: CGPoint, point2: CGPoint) -> FBNormalizedLine {

  var line = FBNormalizedLine(a: point1.y - point2.y, b: point2.x - point1.x, c: point1.x * point2.y - point2.x * point1.y)

  let distance = sqrt(line.b * line.b + line.a * line.a)

  // GPC: prevent divide-by-zero from putting NaNs into the values which cause trouble further on. I'm not sure
  // what cases trigger this, but sometimes point1 == point2 so distance is 0.
  if distance != 0.0 {
    line.a /= distance
    line.b /= distance
    line.c /= distance
  } else {
    line.a = 0
    line.b = 0
    line.c = 0
  }

  return line;
}


func FBNormalizedLineMakeWithCoefficients(a: CGFloat, b: CGFloat, c: CGFloat) -> FBNormalizedLine
{
  return FBNormalizedLine(a: a, b: b, c: c)
}


func FBNormalizedLineOffset(line: FBNormalizedLine, offset: CGFloat) -> FBNormalizedLine
{
  var offsetLine = line
  offsetLine.c = line.c + offset;
  return offsetLine;
}

func FBNormalizedLineDistanceFromPoint(line: FBNormalizedLine, point: CGPoint) -> CGFloat
{
  return line.a * point.x + line.b * point.y + line.c;
}

func FBNormalizedLineIntersection(line1: FBNormalizedLine, line2: FBNormalizedLine) -> CGPoint
{
  let denominator = line1.a * line2.b - line2.a * line1.b
  let x = (line1.b * line2.c - line2.b * line1.c) / denominator
  let y = (line1.a * line2.c - line2.a * line1.c) / denominator
  return CGPointMake(x, y)
}


